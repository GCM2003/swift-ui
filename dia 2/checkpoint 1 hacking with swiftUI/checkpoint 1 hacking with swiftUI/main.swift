// descripción del programa: este programa convierte una temperatura constante de grados celsuis a farenheith
import Foundation

let temperaturaC:Double=36.5 // instanciamos la constante en grados celsius
var temperaturaF:Double // instanciamos una variable para la temperatura en grados farenheith
temperaturaF=(temperaturaC*9/5)+32 // realizamos la conversion de celsius a farenheith
print ("\(temperaturaC)ºC son \(temperaturaF)ºF") // imprimimos el resultado
